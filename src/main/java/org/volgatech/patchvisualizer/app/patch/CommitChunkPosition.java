package org.volgatech.patchvisualizer.app.patch;

public class CommitChunkPosition {
    private int offset;
    private int height;

    public void setOffset(int offset)
    {
        this.offset = offset;
    }

    public int getOffset()
    {
        return offset;
    }

    public void setHeight(int height)
    {
        this.height = height;
    }

    public int getHeight()
    {
        return height;
    }
}
